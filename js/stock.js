$(document).ready(function(){
    $(".stockEssenceTable").show();
    $(".stockEessence").addClass("btn-info");
    $(".stockBottleTable").hide();
    $(".stockItemTable").hide();
    $(".stockSupplierTable").hide();
    $("#searchEssence").focus();
    $(".stockEessence").click(function(){
        $(".stockEssenceTable").show(250);
        $(".stockBottleTable").hide(250);
        $(".stockItemTable").hide(250);
        $(".stockSupplierTable").hide(250);
        $(".stockEessence").addClass("btn-info");
        $(".stockBottle").removeClass("btn-info");
        $(".stockBottle").addClass("btn-default");
        $(".stockItem").removeClass("btn-info");
        $(".stockItem").addClass("btn-default");
        $(".stockSupplier").removeClass("btn-info");
        $(".stockSupplier").addClass("btn-default");
        $("#searchEssence").focus();
    });
    $(".stockBottle").click(function(){
        $(".stockEssenceTable").hide(250);
        $(".stockBottleTable").show(250);
        $(".stockItemTable").hide(250);
        $(".stockSupplierTable").hide(250);
        $(".stockBottle").addClass("btn-info");
        $(".stockEessence").removeClass("btn-info");
        $(".stockEessence").addClass("btn-default");
        $(".stockItem").removeClass("btn-info");
        $(".stockItem").addClass("btn-default");
        $(".stockSupplier").removeClass("btn-info");
        $(".stockSupplier").addClass("btn-default");
        $("#searchBottle").focus();
    });
    $(".stockItem").click(function(){
        $(".stockEssenceTable").hide(250);
        $(".stockBottleTable").hide(250);
        $(".stockItemTable").show(250);
        $(".stockSupplierTable").hide(250);
        $(".stockItem").addClass("btn-info");
        $(".stockBottle").removeClass("btn-info");
        $(".stockBottle").addClass("btn-default");
        $(".stockEessence").removeClass("btn-info");
        $(".stockEessence").addClass("btn-default");
        $(".stockSupplier").removeClass("btn-info");
        $(".stockSupplier").addClass("btn-default");
        $("#searchItem").focus();
    });
});
